  # POST /resource
  def create
    @user = User.new(sign_up_params)

    if @user.save
      sign_up_type = params[:type][:type].downcase
      name = params[:type][:name]
      if sign_up_type == 'client'
        Client.create!(name: name, user: @user)
      elsif sign_up_type == 'driver'
        Driver.create!(name: name, user: @user)
      else
        redirect_to root_url, alert: 'Woops there was something wrong with our form. Try again'
      end
      set_flash_message! :notice, :signed_up
      sign_up(:user, @user)
      redirect_to root_url
    else
      render new
    end
  end

